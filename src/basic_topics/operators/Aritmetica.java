package basic_topics.operators;

public class Aritmetica {
    static void main() {
        System.out.println("*** Operadores aritméticos ***");
        double a = 5, b= 3, result = 0;

        result = a + b;
        System.out.println("Resultado de suma = " + result);

        result = a - b;
        System.out.println("Resultado de resta = " + result);

        result = a * b;
        System.out.println("Resultado de multiplicación = " + result);

        result = a / b;
        System.out.println("Resultado de división = " + result);

        result = a % b;
        System.out.println("Resultado de módulo = " + result);
    }
}
